CREATE DATABASE CourseReviews (
    course VARCHAR(12)
    instructor VARCHAR(24),
    term VARCHAR(6),
    year YEAR,
    workload FLOAT,
    grade VARCHAR(2).
    overall DECIMAL(2, 1),
    organization DECIMAL(2, 1),
    grading DECIMAL(2, 1),
    interesting DECIMAL(2, 1),
    useful DECIMAL(2, 1),
    review VARCHAR(4000)
);
